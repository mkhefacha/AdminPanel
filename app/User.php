<?php

namespace App;

use Carbon\Carbon;
use App\ContactCompany;
use App\Role;
use Hash;
use Illuminate\Auth\Notifications\ResetPassword;
use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\SoftDeletes;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Laravel\Passport\HasApiTokens;

class User extends Authenticatable
{
    use SoftDeletes, Notifiable, HasApiTokens;


    public $table = 'users';

    protected $hidden = [
        'password',
        'remember_token',
    ];

    protected $dates = [
        'updated_at',
        'created_at',
        'deleted_at',
        'email_verified_at',
    ];

    protected $fillable = [
        'name',
        'email',
        'company_id',
        'password',
        'active',
        'created_at',
        'updated_at',
        'deleted_at',
        'remember_token',
        'email_verified_at',
    ];

    protected $attributes = [
        'active' => 0
    ];

    public function getActiveAttribute($attribute)
    {
        return
            [
                0 => 'Inactive',
                1 => 'Active'

            ][$attribute];
    }


    public function getEmailVerifiedAtAttribute($value)
    {
        return $value ? Carbon::createFromFormat('Y-m-d H:i:s', $value)->format(config('panel.date_format') . ' ' . config('panel.time_format')) : null;
    }

    public function setEmailVerifiedAtAttribute($value)
    {
        $this->attributes['email_verified_at'] = $value ? Carbon::createFromFormat(config('panel.date_format') . ' ' . config('panel.time_format'), $value)->format('Y-m-d H:i:s') : null;
    }

    public function setPasswordAttribute($input)
    {
        if ($input) {
            $this->attributes['password'] = app('hash')->needsRehash($input) ? Hash::make($input) : $input;
        }
    }

    public function sendPasswordResetNotification($token)
    {
        $this->notify(new ResetPassword($token));
    }

    public function hasRole($role)
    {

        if ($this->roles()->where('title', $role)->first()) {
            return true;
        } else {
            return false;
        }
    }

    public function scopeActiveUser($query, $field)
    {
        return $query->where('company_id', $field);

    }


    public function roles()
    {
        return $this->belongsToMany(Role::class);
    }

    public function company()
    {
        return $this->belongsTo(ContactCompany::class, 'company_id');
    }


    public function listeCompanies()
    {
        return $this->hasMany(ListeCompany::class, 'user_id', 'id');
    }

    public function contactContacts()
    {
        return $this->hasMany(ContactContact::class, 'user_id', 'id');
    }

    public function emailcompanies()
    {
        return $this->hasMany(EmailCompany::class, 'user_id', 'id');
    }


    public function smscompanies()
    {
        return $this->hasMany(SmsCompany::class, 'user_id', 'id');
    }

    public function events()
    {
        return $this->hasMany(Event::class, 'user_id', 'id');
    }


}
